// Destructuring the Math object to extract the value of PI and assign it to a variable named PI.
let {PI} = Math;
console.log(PI); // 3.141592653589793

// Destructuring the console object to extract the log method and assign it to a variable named log.
let {log} = console;
log(PI); // 3.141592653589793

// This de-structures the Math object, and assigns the value of Math.PI to a variable named PI.
// This is a more concise way to write the following:
let PI2 = Math.PI;
console.log(PI2); // 3.141592653589793
